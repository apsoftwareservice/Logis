"use client";
import type React from "react";
import { useEffect, useRef } from "react";
import { CLOSE_DASHBOARD_DROPDOWNS_EVENT } from "@/components/ui/dropdown/dropdownEvents";

interface DropdownProps {
  isOpen: boolean;
  onClose: () => void;
  children: React.ReactNode;
  className?: string;
}

export const Dropdown: React.FC<DropdownProps> = ({
  isOpen,
  onClose,
  children,
  className = "",
}) => {
  const dropdownRef = useRef<HTMLDivElement>(null);

 useEffect(() => {
  const handleClickOutside = (event: MouseEvent) => {
    if (document.querySelector('[data-dropdown-lock-outside]')) return;

    if (
      dropdownRef.current &&
      !dropdownRef.current.contains(event.target as Node) &&
      !(event.target as HTMLElement).closest('.dropdown-toggle')
    ) {
      onClose();
    }
  };

  document.addEventListener("mousedown", handleClickOutside);
  return () => {
    document.removeEventListener("mousedown", handleClickOutside);
  };
}, [onClose]);

 useEffect(() => {
  window.addEventListener(CLOSE_DASHBOARD_DROPDOWNS_EVENT, onClose);

  return () => {
    window.removeEventListener(CLOSE_DASHBOARD_DROPDOWNS_EVENT, onClose);
  };
}, [onClose]);


  if (!isOpen) return null;

  return (
    <div
      ref={dropdownRef}
      className={`absolute z-40  right-0 mt-2  rounded-xl border border-gray-200 bg-white  shadow-theme-lg dark:border-gray-800 dark:bg-gray-dark ${className}`}
    >
      {children}
    </div>
  );
};
